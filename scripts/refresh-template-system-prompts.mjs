const needSsl = /sslmode=require|neon\.tech|vercel/i.test(PG);
const pool = new pg.Pool({
  connectionString: PG,
  ssl: needSsl ? { rejectUnauthorized: false } : undefined,
});

async function main() {
  const { rows: existing } = await pool.query(
    'select id, kind, title, system_prompt from templates order by kind, id'
  );

  const plan = [];
  for (const r of existing) {
    const map = r.kind === 'image' ? IMG : BOOK;
    const next = map[r.title];
    if (!next) {
      plan.push({ ...r, action: 'skip' });
      continue;
    }
    if (next === r.system_prompt) {
      plan.push({ ...r, action: 'noop' });
      continue;
    }
    plan.push({ ...r, action: 'update', next });
  }

  const toUpdate = plan.filter((p) => p.action === 'update');
  const skipped = plan.filter((p) => p.action === 'skip');
  const noop = plan.filter((p) => p.action === 'noop');

  console.log(
    `总 ${existing.length}：将更新 ${toUpdate.length}，无变化 ${noop.length}，未匹配 ${skipped.length}`
  );

  for (const p of toUpdate) {
    console.log(`[${p.kind}] ${p.title}`);
    console.log(`  旧: ${(p.system_prompt || '(null)').slice(0, 80)}${(p.system_prompt || '').length > 80 ? '…' : ''}`);
    console.log(`  新: ${p.next.slice(0, 80)}…`);
  }
  if (skipped.length) {
    console.log('\n未匹配（跳过）：');
    for (const p of skipped) console.log(`  · [${p.kind}] ${p.title}`);
  }

  if (!APPLY) {
    console.log('\n(dry-run) 加 --apply 才会真正 UPDATE');
    await pool.end();
    return;
  }

  for (const p of toUpdate) {
    await pool.query('update templates set system_prompt = $1 where id = $2', [
      p.next,
      p.id,
    ]);
  }
  console.log(`\n✅ 已 UPDATE ${toUpdate.length} 条`);
  await pool.end();
}

main().catch((e) => {
  console.error('❌', e.message);
  process.exit(1);
});
